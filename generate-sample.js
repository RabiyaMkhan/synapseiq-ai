doc.fontSize(11).font('Helvetica').fillColor('#333');
doc.text('This report provides a comprehensive analysis of Q4 2024 business performance across all departments. Key metrics show a 15% increase in overall revenue compared to Q3, with Sales and Marketing departments leading the growth trajectory.');
doc.moveDown(1);

doc.fontSize(16).font('Helvetica-Bold').fillColor('#000').text('Revenue Overview');
doc.moveDown(0.3);
