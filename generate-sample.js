const PDFDocument = require('pdfkit');
const fs = require('fs');

const doc = new PDFDocument({ margin: 50 });
doc.pipe(fs.createWriteStream('C:\\Users\\Dell\\Documents\\project3\\synapseiq-ai\\sample-report.pdf'));

doc.fontSize(24).font('Helvetica-Bold').text('SynapseIQ AI - Sample Report', { align: 'center' });
doc.moveDown(0.5);
doc.fontSize(12).font('Helvetica').fillColor('#666').text('Quarterly Business Performance Analysis - Q4 2024', { align: 'center' });
doc.moveDown(1);

doc.fontSize(16).font('Helvetica-Bold').fillColor('#000').text('Executive Summary');
